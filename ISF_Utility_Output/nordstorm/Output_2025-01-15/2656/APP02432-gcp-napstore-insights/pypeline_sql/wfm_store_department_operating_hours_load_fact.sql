WHEN NOT MATCHED THEN
INSERT (
    STORE_DEPARTMENT_OPERATING_HOURS_ID,
    EFFECTIVE_DAY,
    OPERATING_HOURS_DETAIL_TYPE,
    START_TIME,
    END_TIME,
    DW_SYS_LOAD_TMSTP,
    DW_SYS_UPDT_TMSTP
)
VALUES (
    src.STORE_DEPARTMENT_OPERATING_HOURS_ID,
    src.EFFECTIVE_DAY,
    src.OPERATING_HOURS_DETAIL_TYPE,
    TIME '00:00:00' + CAST (src.START_TIME AS INTEGER) / 1000 * INTERVAL '00:00:01' HOUR TO SECOND,
    TIME '00:00:00' + CAST (src.END_TIME AS INTEGER) / 1000 * INTERVAL '00:00:01' HOUR TO SECOND,
