    SELECT DISTINCT * FROM {db_env}_NAP_STG.STORE_DEPARTMENT_OPERATING_HOURS_DETAILS_STG
    qualify row_number() over (partition BY STORE_DEPARTMENT_OPERATING_HOURS_ID, EFFECTIVE_DAY ORDER BY LAST_UPDATED_AT DESC, KAFKA_LAST_UPDATED_AT DESC) = 1
